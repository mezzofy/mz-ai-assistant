"""
LegalAgent — Leo, the Legal Specialist.

Leo reviews and drafts business contracts, provides jurisdiction-specific legal
advisory, and assesses legal risk across 7 supported jurisdictions:
    Singapore, Hong Kong, Malaysia, UAE, Saudi Arabia, Qatar, Cayman Islands.

Routing:
    Dispatched when chat.py detects legal keywords and sets task["agent"] = "legal",
    or when task["agent_override"] == "legal" (Management Agent delegation).

    can_handle: task["agent_override"] == "legal" OR legal keyword in message
    execute:    classify → route to workflow → append disclaimer → log

LEGAL DISCLAIMER:
    All Leo outputs include a mandatory disclaimer that this is AI-generated
    analysis for reference only and does not constitute professional legal advice.
"""

import logging
from pathlib import Path

from app.agents.base_agent import BaseAgent

logger = logging.getLogger("mezzofy.agents.legal")

# ── Constants ─────────────────────────────────────────────────────────────────

LEGAL_DISCLAIMER = (
    "\n\n---\n"
    "**Legal Disclaimer:** This analysis is generated by Leo, an AI legal assistant, "
    "for reference and informational purposes only. It does not constitute professional "
    "legal advice and should not be relied upon as such. Laws and regulations vary by "
    "jurisdiction and may change. Please consult a qualified solicitor or legal "
    "professional before making any decisions based on this information."
)

LEGAL_TRIGGERS = [
    # Document types
    "contract", "nda", "non-disclosure", "agreement", "mou", "memorandum of understanding",
    "term sheet", "letter of intent", "loi", "shareholder agreement", "shareholders agreement",
    "employment contract", "vendor agreement", "service agreement", "consultancy agreement",
    "ip assignment", "distribution agreement", "licensing agreement",
    # Actions
    "legal advice", "legal advisory", "legal review", "review contract", "review agreement",
    "review document", "draft contract", "draft agreement", "draft nda",
    "legal analysis", "legal risk", "legal opinion", "legal document",
    "contract review", "contract draft", "contract clause",
    # Legal concepts
    "clause", "indemnity", "liability", "force majeure", "governing law",
    "jurisdiction", "dispute resolution", "arbitration", "confidentiality",
    "intellectual property", "ip rights", "termination clause",
    "compliance", "regulatory", "gdpr", "data protection",
    # Jurisdictions (as standalone legal triggers)
    "singapore law", "hong kong law", "malaysia law", "uae law",
    "cayman islands", "cayman law",
]

