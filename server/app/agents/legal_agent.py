    # ── Helper methods ─────────────────────────────────────────────────────────

    def _classify_legal_task(self, message: str) -> str:
        """
        Classify the message into one of 5 legal workflow types.

        Returns one of:
            "review_document", "generate_contract", "extract_clauses",
            "assess_legal_risk", "provide_legal_advice"
        """
        lower = message.lower()

        # Document review signals
        review_signals = [
            "review", "analyse", "analyze", "check this", "look at this",
            "what does this", "explain this contract", "explain this agreement",
            "read this", "evaluate this",
        ]
        if any(s in lower for s in review_signals) and any(
            d in lower for d in ["contract", "agreement", "nda", "document", "clause"]
        ):
            return "review_document"

        # Contract/document generation signals
        draft_signals = [
            "draft", "create", "generate", "write", "prepare", "template",
            "draw up", "make a", "need a contract", "need an agreement",
        ]
        if any(s in lower for s in draft_signals):
            return "generate_contract"

        # Clause extraction signals
        clause_signals = [
            "extract clause", "find clause", "what clause", "which clause",
            "clause library", "all clauses", "list clauses", "show clauses",
        ]
        if any(s in lower for s in clause_signals):
            return "extract_clauses"

        # Risk assessment signals
        risk_signals = [
            "legal risk", "risk assessment", "risk analysis", "risky",
            "flag issues", "red flags", "potential issues", "liability exposure",
            "what are the risks",
        ]
        if any(s in lower for s in risk_signals):
            return "assess_legal_risk"

        # Default: general legal advice / advisory
        return "provide_legal_advice"

    def _classify_jurisdiction(self, message: str) -> str:
        """
        Detect the jurisdiction from the message text.

        Returns the jurisdiction file name key (e.g., "singapore", "hong_kong")
        or "general" if no specific jurisdiction is detected.
        """
        lower = message.lower()
        for keyword, jurisdiction in _JURISDICTION_MAP.items():
            if keyword in lower:
                return jurisdiction
        return "general"

    def _get_jurisdiction_knowledge_file(self, jurisdiction: str) -> Path:
        """
        Return the path to the jurisdiction knowledge file.

        Args:
            jurisdiction: Jurisdiction file name key (e.g., "singapore").

        Returns:
            Path to the .md file (may not exist — caller must check).
        """
        return _KNOWLEDGE_BASE_PATH / "jurisdictions" / f"{jurisdiction}.md"

    def _load_jurisdiction_knowledge(self, jurisdiction: str) -> str:
        """
        Load and return jurisdiction knowledge file contents.

        Returns empty string if the file does not exist or cannot be read.
        Truncates to 3000 chars to avoid filling context.
        """
        if jurisdiction == "general":
            return ""
        kb_file = self._get_jurisdiction_knowledge_file(jurisdiction)
        try:
            if kb_file.exists():
                content = kb_file.read_text(encoding="utf-8")
                return content[:3000]  # Truncate to manage context
        except Exception as e:
            logger.warning(f"LegalAgent: could not load KB file {kb_file}: {e}")
        return ""

    def _load_contract_template(self, message: str, jurisdiction: str) -> str:
        """
        Load the most appropriate contract template from the knowledge base.

        Detects contract type from message, then loads the matching template file.
        Returns empty string if no template is found.
        """
        lower = message.lower()

        # Map contract type keywords to template file names
        type_map = [
            (["nda", "non-disclosure", "confidentiality agreement"], "nda"),
            (["service agreement", "services agreement", "sow", "statement of work"], "service_agreement"),
            (["employment contract", "employment agreement", "job offer"], "employment_contract"),
            (["mou", "memorandum of understanding"], "mou"),
            (["vendor agreement", "supplier agreement"], "vendor_agreement"),
            (["shareholders agreement", "shareholder agreement"], "shareholders_agreement"),
            (["loi", "letter of intent"], "loi"),
        ]

        contract_type = "nda"  # default
        for keywords, template_name in type_map:
            if any(kw in lower for kw in keywords):
                contract_type = template_name
                break

        # Try jurisdiction-specific template first, then general
        for jur in (jurisdiction, "general"):
            template_path = _KNOWLEDGE_BASE_PATH / "templates" / jur / f"{contract_type}.md"
            try:
                if template_path.exists():
                    content = template_path.read_text(encoding="utf-8")
                    return content[:2000]  # Truncate to manage context
            except Exception as e:
                logger.warning(f"LegalAgent: could not load template {template_path}: {e}")

        return ""
